namespace AstroAlgo.Models
{
    /// <summary>
    /// Equator coordinates.
    /// </summary>
    public class Equator
    {
        /// <summary>
        /// Right ascension.
        /// </summary>
        /// <remarks>
        /// Range from 0 to 360.
        /// </remarks>
        public double RA;

        /// <summary>
        /// Declination.
        /// </summary>
        /// <remarks>
        /// Range from -90 to 90.
        /// </remarks>
        public double Dec;
    }
}
